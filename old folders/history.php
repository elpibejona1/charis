					<div class="timeline__event">
						<p>Chad is an extraordinary example of what an African can accomplish when he is filled with the desire to be used of the Lord. In 1998 a training institute was held at the Chateau of Saint Albain for African pastors. Travelling with them was a layman, a builder by trade. The pastors said that he could sit in the library while they took their classes, but of course he was invited to all the courses; the subject was church-planting. After three weeks of studies, he returned to his country, built what he called his Chateau, and began to train evangelists and send them out. Not only were hundreds of new churches planted throughout Chad, but he also sent evangelists to Nigeria as well as to Cameroon. He was on his way to evangelize Libya when because of a breakdown in the Sahara desert he later died.</p>
						<p>Samuel Dadjé will always be remembered as perhaps the most outstanding pioneer of Brethren missions.</p>
