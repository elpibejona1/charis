						<p>The Chateau became a bridge both in evangelism, bringing believers and unbelievers together in personal relationships, and in church planting, allowing new believers to learn what they would later experience in a church. Six churches were planted in surrounding cities during the first two decades of the ministry.</p>
						<p>The Chateau also became a Bible training center, an encounter center for strategic discussions between mission organizations, and a center for a European branch of Grace Theological Seminary. The Chateau was used of the Lord for putting Europe on the map of Grace Brethren missions, with more than 100 missionaries later coming to Europe for varying periods of ministry.</p>
